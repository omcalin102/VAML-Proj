function demo_eval_all()
root = '/MATLAB Drive/VAML_Project_Baseline_Code';
imgDir = fullfile(root,'pedestrian','pedestrian');
gtFile = fullfile(root,'data','test.dataset');
model  = fullfile(root,'results','models','model_baseline.mat');

% 1) sanity check GT vs images
check_dataset_alignment(imgDir, gtFile, fullfile(root,'results','metrics','run4','dataset_alignment.txt'));

% 2) quantitative metrics (CSV + summary + optional PR curve)
eval_metrics_imageset( ...
  'ImageDir', imgDir, ...
  'ModelPath', model, ...
  'RunTag', 'run4', ...                 % new folder: results/metrics/run4
  'BaseWindow', [64 128], ...
  'Step', 16, ...
  'ResizeLongSide', 480, ...
  'MinScore', 0.6, ...
  'NMS_IoU', 0.35, ...
  'GTFile', gtFile, ...
  'IoU_TP', 0.50, ...
  'MakePR', true);

% 3) annotated video built from frames
eval_frames_to_video( ...
  'ImageDir', imgDir, ...
  'ModelPath', model, ...
  'RunTag', 'run4', ...                 % results/videos/run4/pedestrian_run4.mp4
  'FPS', 10, ...
  'BaseWindow',[64 128], 'Step',16, 'ResizeLongSide',480, ...
  'MinScore',0.6, 'NMS_IoU',0.25);

% 4) failure crops for reflection slide
dump_fp_fn_crops(imgDir, gtFile, model, fullfile(root,'results','metrics','run4','fails'), ...
  'BaseWindow',[64 128], 'Step',16, 'ResizeLongSide',480, ...
  'MinScore',0.6, 'NMS_IoU',0.25, 'IoU_TP',0.50);
end
